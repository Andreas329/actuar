#include <R.h>
#include <Rmath.h>
#include "locale.h"

double rinverseexp(double scale)
{
    if (!R_FINITE(scale) ||
	scale <= 0.0)
	error("invalid arguments");

    return scale / log(1.0 / unif_rand());
}
